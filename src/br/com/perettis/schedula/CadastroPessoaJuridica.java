    @Action
    /**
     * Salva novo cadastro ou altera
     */
	public void Salvar() {

        // se id for nulo trata como inclusão
		if (pessoaJuridica.getId() == null) {

            if (!Seguranca.Permitido("Cadastro-PessoaJuridica-inclusao")) {
                JOptionPane.showMessageDialog(null, "Sem permissão");
                return;
            }
            
            pessoaJuridica = new PessoaJuridica();
			t = pessoaJuridicaDAO.getSession().beginTransaction();
			t.begin();
			try {

				// Preenche dado nos objetos
                pessoaJuridica.setBairro(txtBairro.getText());
                
                               
                pessoaJuridica.setCep(txtCep.getText());
                pessoaJuridica.setCidade(txtCidade.getText());
                pessoaJuridica.setCodigoEmpresa(txtCodigoEmpresa.getText());
                pessoaJuridica.setContato1(txtContato1.getText());
                pessoaJuridica.setContato2(txtContato2.getText());
                pessoaJuridica.setContato3(txtContato3.getText());                

                if (!Filters.onlyNumbers(txtCnpj.getText()).equalsIgnoreCase("")) {
                    if (!DocumentsBR.CNPJ(txtCnpj.getText())) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.cnpj",
                                String.valueOf(txtCnpj.getText())));
                        txtCnpj.requestFocus();
                        return;
                    }
                    //Verifica se já existe mesmo cnpj
                    if (pessoaJuridicaDAO.buscaPorCnpj(Filters.onlyNumbers(txtCnpj.getText())) != null) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("jaexiste.cnpj",
                                String.valueOf(txtCnpj.getText())));
                        txtCnpj.requestFocus();
                        return;
                    }
                    pessoaJuridica.setCnpj(Filters.onlyNumbers(txtCnpj.getText()));
                }else{
                    pessoaJuridica.setCnpj(null);
                }

                
                //TODO ARRUMAR FOCOS DO ERRO DO EMAIL
                //TODO CONTINUAR COM TROCAS PARA PESSOA JURIDICA
                if ((txtEmail.getText().length() > 0)&& (!Email.validSintax(txtEmail.getText()))){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.email", txtEmail.getText()));
                        txtEmail.requestFocus();
                        return;                    
                }
                pessoaJuridica.setEmail(txtEmail.getText());
                pessoaJuridica.setEndereco(txtEndereco.getText());
                pessoaJuridica.setFone1(txtFone1.getText());
                pessoaJuridica.setFone2(txtFone2.getText());
                pessoaJuridica.setFone3(txtFone3.getText());
                
                if(txtNome.getText().length() <=0){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.nome",txtNome.getText()));
                        txtNome.requestFocus();
                        return;                    
                }
                pessoaJuridica.setNome(txtNome.getText());
                
                pessoaJuridica.setObservacao(txtObservacao.getText());

                
                if (!txtInscEstadual.getText().equalsIgnoreCase("")) {
                    pessoaJuridica.setInscEstadual(txtInscEstadual.getText());
                }else{
                    pessoaJuridica.setInscEstadual(null);
                }

                if (!txtInscMunicipal.getText().equalsIgnoreCase("")) {
                    pessoaJuridica.setInscMunicipal(txtInscMunicipal.getText());
                }else{
                    pessoaJuridica.setInscMunicipal(null);
                }

                
                pessoaJuridica.setUf((Uf) cbUf.getSelectedItem());

                pessoaJuridica.setSocios(txtSocios.getText());
                
                // Grava 
				pessoaJuridicaDAO.makePersistent(pessoaJuridica);
				t.commit();
                
                // Confirma
				JOptionPane.showMessageDialog(this,
					resourceMap.getString("cadastro.sucesso", 
						String.valueOf(pessoaJuridica.getId())));
                setUltimoCadastro(pessoaJuridica);
                
                // Reseta
                pessoaJuridica = null;
                ResetaForm();
			} catch (ExceptionInInitializerError e) {
                // Se ocorre algum erro, mostra mensagem e desfaz transação
				JOptionPane.showMessageDialog(this,
					resourceMap.getString("cadastro.erro") +
					e.getLocalizedMessage());
				t.rollback();
			}

		// Se for alteração
		} else {
         
            if (!Seguranca.Permitido("Cadastro-PessoaJuridica-alteracao")) {
                JOptionPane.showMessageDialog(null, "Sem permissão");
                return;
            }
            
            try {
                // Preenche dado nos objetos
                pessoaJuridica.setBairro(txtBairro.getText());
                
                pessoaJuridica.setCep(txtCep.getText());
                pessoaJuridica.setCidade(txtCidade.getText());
                pessoaJuridica.setCodigoEmpresa(txtCodigoEmpresa.getText());
                pessoaJuridica.setContato1(txtContato1.getText());
                pessoaJuridica.setContato2(txtContato2.getText());
                pessoaJuridica.setContato3(txtContato3.getText());                

                if (!Filters.onlyNumbers(txtCnpj.getText()).equalsIgnoreCase("")) {
                    if (!DocumentsBR.CNPJ(txtCnpj.getText())) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.cnpj",
                                String.valueOf(txtCnpj.getText())));
                        txtCnpj.requestFocus();
                        return;
                    }
                    
                    
                    // Verifica se existe outro com mesmos dados
                    PessoaJuridica outro = pessoaJuridicaDAO.buscaPorCnpj(
                            Filters.onlyNumbers(txtCnpj.getText()));
                    if (outro != null) {
                        if (outro.getId() != pessoaJuridica.getId()) {

                            JOptionPane.showMessageDialog(this,
                                    resourceMap.getString("jaexiste.cnpj",
                                    String.valueOf(txtCnpj.getText())));
                            txtCnpj.requestFocus();
                            return;
                        }
                    }
                    pessoaJuridica.setCnpj(Filters.onlyNumbers(txtCnpj.getText()));
                } else {
                    pessoaJuridica.setCnpj(null);
                }

                if ((txtEmail.getText().length() > 0)&& (!Email.validSintax(txtEmail.getText()))){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.email",txtEmail.getText()));
                        txtEmail.requestFocus();
                        return;                    
                }
                pessoaJuridica.setEmail(txtEmail.getText());

                pessoaJuridica.setEndereco(txtEndereco.getText());
                pessoaJuridica.setFone1(txtFone1.getText());
                pessoaJuridica.setFone2(txtFone2.getText());
                pessoaJuridica.setFone3(txtFone3.getText());
                
                if(txtNome.getText().length() <=0){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.nome"));
                        txtNome.requestFocus();
                        return;                    
                }
                pessoaJuridica.setNome(txtNome.getText());
                
                pessoaJuridica.setObservacao(txtObservacao.getText());
                
                if (!txtInscEstadual.getText().equalsIgnoreCase("")) {
                    pessoaJuridica.setInscEstadual(txtInscEstadual.getText());
                }else{
                    pessoaJuridica.setInscEstadual(null);
                }

                if (!txtInscMunicipal.getText().equalsIgnoreCase("")) {
                    pessoaJuridica.setInscMunicipal(txtInscMunicipal.getText());
                }else{
                    pessoaJuridica.setInscMunicipal(null);
                }
                pessoaJuridica.setUf((Uf) cbUf.getSelectedItem());
                
                pessoaJuridica.setSocios(txtSocios.getText());
                
                // Grava alterações
				pessoaJuridicaDAO.makePersistent(pessoaJuridica);
				t.commit();
                
                // Mostra confirmação
				JOptionPane.showMessageDialog(this, 
                        resourceMap.getString("altera.sucesso"));
                
                // Reinicia formulario
                pessoaJuridica = null;
				ResetaForm();

			} catch (Exception e) {
                // Qualquer erro mostra mensagem e desfaz transação
				JOptionPane.showMessageDialog(this, 
                        resourceMap.getString("altera.erro") +
                        e.getLocalizedMessage());
				t.rollback();
                e.printStackTrace();
			}
		}
	}

    /**
     * Abre cadastro no formulário para alteração
     */
	public void Alterar() {
        
        // Busca nova sessao
		pessoaJuridicaDAO = daoFactory.getPessoaJuridicaDAO();
		try {
            // Inicia transação
			t = pessoaJuridicaDAO.getSession().beginTransaction();
            
            // Busca objeto pelo id informado
            pessoaJuridica = pessoaJuridicaDAO.findById(
                    Long.valueOf(txtId.getText()), true);
            
            // Preenche dados no formulário
            txtBairro.setText(pessoaJuridica.getBairro());
            txtCep.setText(pessoaJuridica.getCep());
            txtCidade.setText(pessoaJuridica.getCidade());
            txtCodigoEmpresa.setText(pessoaJuridica.getCodigoEmpresa());
            txtContato1.setText(pessoaJuridica.getContato1());
            txtContato2.setText(pessoaJuridica.getContato2());
            txtContato3.setText(pessoaJuridica.getContato3());
            if (pessoaJuridica.getCnpj()!=null) {
                txtCnpj.setText(pessoaJuridica.getCnpj());
            }else{
                txtCnpj.setText("");
            }
            txtEmail.setText(pessoaJuridica.getEmail());
            txtEndereco.setText(pessoaJuridica.getEndereco());
            txtFone1.setText(pessoaJuridica.getFone1());
            txtFone2.setText(pessoaJuridica.getFone2());
            txtFone3.setText(pessoaJuridica.getFone3());
            txtNome.setText(pessoaJuridica.getNome());
            txtObservacao.setText(pessoaJuridica.getObservacao());
            txtInscEstadual.setText(pessoaJuridica.getInscEstadual());
            txtInscMunicipal.setText(pessoaJuridica.getInscMunicipal());

            txtSocios.setText(pessoaJuridica.getSocios());
            cbUf.setSelectedItem(pessoaJuridica.getUf());
            
            
            // Ativa controles disponíveis
			btnSalvar.setEnabled(true);
			btnExcluir.setEnabled(true);
            txtId.setEnabled(false);

		} catch (Exception e) {
            e.printStackTrace();
            // Se não acha, nostra mensagem
			JOptionPane.showMessageDialog(this, 
                    resourceMap.getString("altera.errobusca", txtId.getText()));
			txtId.setEnabled(true);
			btnSalvar.setEnabled(false);
			btnExcluir.setEnabled(false);
			txtId.requestFocus();
		}
	}
    
