    private void cbUfMonta() {
        UfDAO ufDAO =  getDaoFactory().getUfDAO();
        Transaction tuf = ufDAO.getSession().getTransaction();
        tuf.begin();
        List<Uf> list = ufDAO.findAll();
        Iterator<Uf> it = list.iterator();
        while (it.hasNext()) {
            cbUf.addItem(it.next());
        }
    }

