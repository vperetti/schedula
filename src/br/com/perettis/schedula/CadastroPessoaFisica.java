    private void cbEstadoCivilMonta() {
        EstadoCivilDAO estadoCivilDAO =  getDaoFactory().getEstadoCivilDAO();
        Transaction t = estadoCivilDAO.getSession().getTransaction();
        t.begin();
        List<EstadoCivil> esList = estadoCivilDAO.findByPropertyLike("desabilitado", false, DAOConstants.EQUAL, "descricao");
        Iterator<EstadoCivil> itEs = esList.iterator();
        while (itEs.hasNext()) {
            cbEstadoCivil.addItem(itEs.next());
        }
    }
    
   
    private void cbUfMonta() {
        UfDAO ufDAO =  getDaoFactory().getUfDAO();
        Transaction tuf = ufDAO.getSession().getTransaction();
        tuf.begin();
        List<Uf> list = ufDAO.findAll();
        Iterator<Uf> it = list.iterator();
        while (it.hasNext()) {
            cbUf.addItem(it.next());
        }
    }

