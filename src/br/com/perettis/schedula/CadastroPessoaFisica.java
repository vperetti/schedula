    	@Action
    /**
     * Salva novo cadastro ou altera
     */
	public void Salvar() {

        // se id for nulo trata como inclusão
		if (pessoaFisica.getId() == null) {
            
            if (!Seguranca.Permitido("Cadastro-PessoaFisica-inclusao")) {
                JOptionPane.showMessageDialog(null, "Sem permissão");
                return;
            }

            
            pessoaFisica = new PessoaFisica();
			t = pessoaFisicaDAO.getSession().beginTransaction();
			t.begin();
			try {

				// Preenche dado nos objetos
                pessoaFisica.setBairro(txtBairro.getText());
                
                
                //TODO COLOCAR NO DAO PESSOA FISICA BUSCA POR CARTEIRA DE TRABALHO E VERIFICAR AQUI
                if (!txtCT.getText().equalsIgnoreCase("")){
                    pessoaFisica.setCarteiraTrabalho(txtCT.getText());
                }else{
                    pessoaFisica.setCarteiraTrabalho(null);
                }
                if (!txtCTSerie.getText().equalsIgnoreCase("")){
                    pessoaFisica.setCarteiraTrabalhoSerie(txtCTSerie.getText());
                }else{
                    pessoaFisica.setCarteiraTrabalhoSerie(null);
                }
                
                pessoaFisica.setCep(txtCep.getText());
                pessoaFisica.setCidade(txtCidade.getText());
                pessoaFisica.setContato1(txtContato1.getText());
                pessoaFisica.setContato2(txtContato2.getText());
                pessoaFisica.setContato3(txtContato3.getText());                

                if (!Filters.onlyNumbers(txtCpf.getText()).equalsIgnoreCase("")) {
                    if (!DocumentsBR.CPF(txtCpf.getText())) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.cpf",
                                String.valueOf(txtCpf.getText())));
                        txtCpf.requestFocus();
                        return;
                    }
                    //Verifica se já existe mesmo cpf
                    if (pessoaFisicaDAO.buscaPorCpf(Filters.onlyNumbers(txtCpf.getText())) != null) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("jaexiste.cpf",
                                String.valueOf(txtCpf.getText())));
                        txtCpf.requestFocus();
                        return;
                    }
                    pessoaFisica.setCpf(Filters.onlyNumbers(txtCpf.getText()));
                }else{
                    pessoaFisica.setCpf(null);
                }

                
                
                if (jxdDataNascimento.getDate() != null) {
                    if (jxdDataNascimento.getDate().after(new Date())) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.dataNascimento"));
                        jxdDataNascimento.requestFocus();
                        return;
                    }
                    pessoaFisica.setDataNascimento(jxdDataNascimento.getDate());
                }
                
                
                if ((txtEmail.getText().length() > 0)&& (!Email.validSintax(txtEmail.getText()))){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.email", txtEmail.getText()));
                        txtEmail.requestFocus();
                        return;                    
                }
                pessoaFisica.setEmail(txtEmail.getText());
                pessoaFisica.setEndereco(txtEndereco.getText());
                pessoaFisica.setEstadoCivil((EstadoCivil) cbEstadoCivil.getSelectedItem());
                pessoaFisica.setFone1(txtFone1.getText());
                pessoaFisica.setFone2(txtFone2.getText());
                pessoaFisica.setFone3(txtFone3.getText());
                pessoaFisica.setNacionalidade(txtNacionalidade.getText());
                pessoaFisica.setNaturalidade(txtNaturalidade.getText());
                
                if(txtNome.getText().length() <=0){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.nome",txtNome.getText()));
                        txtNome.requestFocus();
                        return;                    
                }
                pessoaFisica.setNome(txtNome.getText());
                
                pessoaFisica.setNomeMae(txtNomeMae.getText());
                pessoaFisica.setNomePai(txtNomePai.getText());
                pessoaFisica.setObservacao(txtObservacao.getText());
                pessoaFisica.setProfissao(txtProfissao.getText());

                //TODO COLOCAR BUSCA POR RG E COLOCAR AQUI
                if (!txtRG.getText().equalsIgnoreCase("")) {
                    pessoaFisica.setRg(txtRG.getText());
                }else{
                    pessoaFisica.setRg(null);
                }
                if (!txtOE.getText().equalsIgnoreCase("")) {
                    pessoaFisica.setRgOrgaoExpeditor(txtOE.getText());
                }else{
                    pessoaFisica.setRgOrgaoExpeditor(null);
                }
                
                pessoaFisica.setSexo(rbMasculino.isSelected());
                pessoaFisica.setUf((Uf) cbUf.getSelectedItem());
                
                
                // Grava 
				pessoaFisicaDAO.makePersistent(pessoaFisica);
				t.commit();
                
                // Confirma
				JOptionPane.showMessageDialog(this,
					resourceMap.getString("cadastro.sucesso", 
						String.valueOf(pessoaFisica.getId())));
                
                setUltimoCadastro(pessoaFisica);
                // Reseta
                pessoaFisica = null;
                ResetaForm();
			} catch (ExceptionInInitializerError e) {
                // Se ocorre algum erro, mostra mensagem e desfaz transação
				JOptionPane.showMessageDialog(this,
					resourceMap.getString("cadastro.erro") +
					e.getLocalizedMessage());
				t.rollback();
			}

		// Se for alteração
		} else {
            
            if (!Seguranca.Permitido("Cadastro-PessoaFisica-alteracao")) {
                JOptionPane.showMessageDialog(null, "Sem permissão");
                return;
            }

            
            try {
                // Preenche dado nos objetos
                pessoaFisica.setBairro(txtBairro.getText());
                
                //TODO COLOCAR NO DAO PESSOA FISICA BUSCA POR CARTEIRA DE TRABALHO E VERIFICAR AQUI
                if (!txtCT.getText().equalsIgnoreCase("")){
                    pessoaFisica.setCarteiraTrabalho(txtCT.getText());
                }else{
                    pessoaFisica.setCarteiraTrabalho(null);
                }
                if (!txtCTSerie.getText().equalsIgnoreCase("")){
                    pessoaFisica.setCarteiraTrabalhoSerie(txtCTSerie.getText());
                }else{
                    pessoaFisica.setCarteiraTrabalhoSerie(null);
                }
                
                pessoaFisica.setCep(txtCep.getText());
                pessoaFisica.setCidade(txtCidade.getText());
                pessoaFisica.setContato1(txtContato1.getText());
                pessoaFisica.setContato2(txtContato2.getText());
                pessoaFisica.setContato3(txtContato3.getText());                

                if (!Filters.onlyNumbers(txtCpf.getText()).equalsIgnoreCase("")) {
                    if (!DocumentsBR.CPF(txtCpf.getText())) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.cpf",
                                String.valueOf(txtCpf.getText())));
                        txtCpf.requestFocus();
                        return;
                    }
                    
                    
                    // Verifica se existe outro com mesmos dados
                    PessoaFisica outro = pessoaFisicaDAO.buscaPorCpf(Filters.onlyNumbers(txtCpf.getText()));
                    if (outro != null) {
                        if (outro.getId() != pessoaFisica.getId()) {

                            JOptionPane.showMessageDialog(this,
                                    resourceMap.getString("jaexiste.cpf",
                                    String.valueOf(txtCpf.getText())));
                            txtCpf.requestFocus();
                            return;
                        }
                    }
                    pessoaFisica.setCpf(Filters.onlyNumbers(txtCpf.getText()));
                } else {
                    pessoaFisica.setCpf(null);
                }

                if (jxdDataNascimento.getDate() != null) {
                    if (jxdDataNascimento.getDate().after(new Date())) {
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.dataNascimento"));
                        jxdDataNascimento.requestFocus();
                        return;
                    }
                    pessoaFisica.setDataNascimento(jxdDataNascimento.getDate());
                }

                if ((txtEmail.getText().length() > 0)&& (!Email.validSintax(txtEmail.getText()))){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.email",txtEmail.getText()));
                        txtEmail.requestFocus();
                        return;                    
                }
                pessoaFisica.setEmail(txtEmail.getText());

                pessoaFisica.setEndereco(txtEndereco.getText());
                pessoaFisica.setEstadoCivil((EstadoCivil) cbEstadoCivil.getSelectedItem());
                pessoaFisica.setFone1(txtFone1.getText());
                pessoaFisica.setFone2(txtFone2.getText());
                pessoaFisica.setFone3(txtFone3.getText());
                pessoaFisica.setNacionalidade(txtNacionalidade.getText());
                pessoaFisica.setNaturalidade(txtNaturalidade.getText());
                
                if(txtNome.getText().length() <=0){
                        JOptionPane.showMessageDialog(
                                this,
                                resourceMap.getString("invalido.nome"));
                        txtNome.requestFocus();
                        return;                    
                }
                pessoaFisica.setNome(txtNome.getText());
                
                pessoaFisica.setNomeMae(txtNomeMae.getText());
                pessoaFisica.setNomePai(txtNomePai.getText());
                pessoaFisica.setObservacao(txtObservacao.getText());
                pessoaFisica.setProfissao(txtProfissao.getText());
                
                if (!txtRG.getText().equalsIgnoreCase("")) {
                    pessoaFisica.setRg(txtRG.getText());
                }else{
                    pessoaFisica.setRg(null);
                }
                if (!txtOE.getText().equalsIgnoreCase("")) {
                    pessoaFisica.setRgOrgaoExpeditor(txtOE.getText());
                }else{
                    pessoaFisica.setRgOrgaoExpeditor(null);
                }                pessoaFisica.setSexo(rbMasculino.isSelected());
                pessoaFisica.setUf((Uf) cbUf.getSelectedItem());
                
                // Grava alterações
				pessoaFisicaDAO.makePersistent(pessoaFisica);
				t.commit();
                
                // Mostra confirmação
				JOptionPane.showMessageDialog(this, 
                        resourceMap.getString("altera.sucesso"));
                
                // Reinicia formulario
                pessoaFisica = null;
				ResetaForm();

			} catch (Exception e) {
                // Qualquer erro mostra mensagem e desfaz transação
				JOptionPane.showMessageDialog(this, 
                        resourceMap.getString("altera.erro") +
                        e.getLocalizedMessage());
				t.rollback();
                e.printStackTrace();
			}
		}
	}

