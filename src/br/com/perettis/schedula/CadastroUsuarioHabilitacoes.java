	@Action
    /**
     * Salva novo cadastro ou altera
     */
	public void Salvar() {

        // se id for nulo trata como inclusão
		if (usuario.getId() == null) {
               
            if (!Seguranca.Permitido("Suporte-CadastroUsuario-inclusao")) {
                JOptionPane.showMessageDialog(null, "Sem permissão");
                return;
            }
            
            usuario = new Usuario();
			t = UsuarioDAO.getSession().beginTransaction();
			t.begin();
			try {
                //Verifica se já existe mesma descrição
				if (UsuarioDAO.buscaPorApelido(txtApelido.getText())
						!= null) {
					JOptionPane.showMessageDialog(
						this,
						resourceMap.getString("jaexiste.apelido", 
							String.valueOf(txtApelido.getText())));
					txtApelido.requestFocus();
					return;
				}

				// Preenche dado nos objetos
				usuario.setApelido(txtApelido.getText());
                //TODO usar metodo getPassword, preciso converter char to byte
                if (!txtSenha.getText().equalsIgnoreCase("")){
                    usuario.setSenha(Seguranca.CriptografaSenha(txtSenha.getText()));
                }
                usuario.setNome(txtNome.getText());
                usuario.setEmail(txtEmail.getText());
                usuario.setAdministrador(cbAdministrador.isSelected());
				usuario.setDesabilitado(cbDesabilitado.isSelected());
                
                
              
                // Grava 
				UsuarioDAO.makePersistent(usuario);
				t.commit();

                
                
                UsuarioDireitoDAO usuarioDireitoDAO = daoFactory.getUsuarioDireitoDAO();
                t = usuarioDireitoDAO.getSession().getTransaction();
                t.begin();
                Object[] aDireitos = lmDireitos.toArray();
                for (int i = 0; i < aDireitos.length; i++) {
                    UsuarioDireito direito = (UsuarioDireito)aDireitos[i];
                    direito.setUsuario(usuario);
                    usuarioDireitoDAO.makePersistent(direito);
                }
                t.commit();
                
                
                // Confirma
				JOptionPane.showMessageDialog(this,
					resourceMap.getString("cadastro.sucesso", 
						String.valueOf(usuario.getId())));
                // Reseta
                usuario = null;
                ResetaForm();
			} catch (Exception e) {
                // Se ocorre algum erro, mostra mensagem e desfaz transação
				JOptionPane.showMessageDialog(this,
					resourceMap.getString("cadastro.erro") +
					e.getLocalizedMessage());
				t.rollback();
			}


		// Se for alteração
		} else {
            
            if (!Seguranca.Permitido("Suporte-CadastroUsuario-alteracao")) {
                JOptionPane.showMessageDialog(null, "Sem permissão");
                return;
            }
            
            try {
                // Verifica se existe outro com mesmos dados
                Usuario outro = UsuarioDAO.buscaPorApelido(
                        txtApelido.getText());
                if (outro != null) {
                    if (outro.getId() != usuario.getId()) {

                        JOptionPane.showMessageDialog(this,
                                resourceMap.getString("jaexiste.apelido",
                                String.valueOf(txtApelido.getText())));
                        txtApelido.requestFocus();
                        return;
                    }
                }

				// Preenche dados do form no objeto
				usuario.setApelido(txtApelido.getText());
                //TODO usar metodo getPassword, preciso converter char to byte
                if (!txtSenha.getText().equalsIgnoreCase("")){
                    usuario.setSenha(Seguranca.CriptografaSenha(txtSenha.getText()));
                }
                usuario.setNome(txtNome.getText());
                usuario.setEmail(txtEmail.getText());
                usuario.setAdministrador(cbAdministrador.isSelected());
				usuario.setDesabilitado(cbDesabilitado.isSelected());


                
                Set<UsuarioDireito> direitos = new LinkedHashSet<UsuarioDireito>();// = usuario.getDireitos();
                direitos.clear();
                Object[] aDireitos = lmDireitos.toArray();
                for (int i = 0; i < aDireitos.length; i++) {
                    direitos.add((UsuarioDireito)aDireitos[i]);
                    //usuarioDireitoDAO.makePersistent(direito);
                }
                usuario.setDireitos(direitos);
                
                // Grava alterações
				UsuarioDAO.makePersistent(usuario);
				t.commit();
                

                
                // Mostra confirmação
				JOptionPane.showMessageDialog(this, 
                        resourceMap.getString("altera.sucesso"));
                
                // Reinicia formulario
                usuario = null;
				ResetaForm();

			} catch (Exception e) {
                // Qualquer erro mostra mensagem e desfaz transação
				JOptionPane.showMessageDialog(this, 
                        resourceMap.getString("altera.erro") +
                        e.getLocalizedMessage());
				t.rollback();
			}
		}
	}

    /**
     * Abre cadastro no formulário para alteração
     */
	public void Alterar() {
        // Busca nova sessao
		UsuarioDAO = daoFactory.getUsuarioDAO();
		try {
            // Inicia transação
			t = UsuarioDAO.getSession().beginTransaction();
            
            // Busca objeto pelo id informado
            usuario = UsuarioDAO.findById(
                    Long.valueOf(txtId.getText()), true);
            
            // Preenche dados no formulário
            txtApelido.setText(usuario.getApelido());
            txtNome.setText(usuario.getNome());
            txtEmail.setText(usuario.getEmail());
            cbAdministrador.setSelected(usuario.isAdministrador());
            jTabbedPane1.setEnabledAt(1, !usuario.isAdministrador());
            
            cbDesabilitado.setSelected(usuario.isDesabilitado());
            
            Set<UsuarioDireito> direitos = usuario.getDireitos();
            for (UsuarioDireito usuarioDireito : direitos) {
                lmDireitos.addElement(usuarioDireito);
            }
            // Ativa controles disponíveis
			btnSalvar.setEnabled(true);
			btnExcluir.setEnabled(true);
            txtId.setEnabled(false);
            jTabbedPane1.setSelectedIndex(0);

		} catch (Exception e) {
            // Se não acha, nostra mensagem
			JOptionPane.showMessageDialog(this, 
                    resourceMap.getString("altera.errobusca", txtId.getText()));
			txtId.setEnabled(true);
			btnSalvar.setEnabled(false);
			btnExcluir.setEnabled(false);
			txtId.requestFocus();
		}
	}

