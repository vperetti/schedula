    private void cbRotinasMonta() {
        SchedulaRotinaDAO schedulaRotinaDAO =  getDaoFactory().getSchedulaRotinaDAO();
        Transaction t = schedulaRotinaDAO.getSession().getTransaction();
        t.begin();
        //List<SchedulaRotina> esList = schedulaRotinaDAO.findByPropertyLike("desabilitado", false, DAOConstants.EQUAL, "nome");
        List<SchedulaRotina> esList = schedulaRotinaDAO.buscaLista();
        Iterator<SchedulaRotina> itEs = esList.iterator();
        while (itEs.hasNext()) {
            cbRotinas.addItem(itEs.next());
        }
    }
	
    
