private void btnAddHabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddHabActionPerformed
    UsuarioDireito usuarioDireito = new UsuarioDireito();
    usuarioDireito.setRotina((SchedulaRotina) cbRotinas.getSelectedItem());
    if ((usuario.getId() != null) && (usuario.getId() > 0)) {
        usuarioDireito.setUsuario(usuario);
    }
    if (!lmDireitos.isEmpty()) {
        Object[] aVer = lmDireitos.toArray();
        for (int i = 0; i < aVer.length; i++) {
            if (((UsuarioDireito) aVer[i]).getRotina().getId() == usuarioDireito.getRotina().getId()) {
                JOptionPane.showMessageDialog(this, "Usuário já possui direito sobre essa rotina");
                return;
            }
        }
    }
    lmDireitos.addElement(usuarioDireito);
}//GEN-LAST:event_btnAddHabActionPerformed

private void btnRemHabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemHabActionPerformed
    int[] aToDel = lstDireitos.getSelectedIndices();

    for (int i = 0; i < aToDel.length; i++) {
        lmDireitos.removeElementAt(aToDel[i] - i);
    }

    if (usuario.getId() > 0) {
        Object[] aDireitos = lstDireitos.getSelectedValues();
        for (int i = 0; i < aDireitos.length; i++) {
            usuario.getDireitos().remove(aDireitos[i]);
        }
    }
}//GEN-LAST:event_btnRemHabActionPerformed

