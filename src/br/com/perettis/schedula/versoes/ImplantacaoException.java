package br.com.perettis.schedula.versoes;

/**
 *
 * @author Vinicius Peretti
 */
public class ImplantacaoException extends Exception {

    /**
     * Creates a new instance of <code>ImplantacaoException</code> without detail message.
     */
    public ImplantacaoException() {
    }


    /**
     * Constructs an instance of <code>ImplantacaoException</code> with the specified detail message.
     * @param msg the detail message.
     */
    public ImplantacaoException(String msg) {
        super(msg);
    }
}
