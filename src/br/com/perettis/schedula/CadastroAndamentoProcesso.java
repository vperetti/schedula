    private void cbAcompanhamentoTipoMonta() {
        cbAcompanhamentoTipo.removeAllItems();
        AcompanhamentoTipoDAO acompanhamentoTipoDAO =  getDaoFactory().getAcompanhamentoTipoDAO();
        Transaction tcbm = acompanhamentoTipoDAO.getSession().getTransaction();
        tcbm.begin();
        List<AcompanhamentoTipo> list = acompanhamentoTipoDAO.findAll();
        Iterator<AcompanhamentoTipo> it = list.iterator();
        while (it.hasNext()) {
            cbAcompanhamentoTipo.addItem(it.next());
        }
    }
    
    private void cbAudienciaTipoMonta() {
        cbAudienciaTipo.removeAllItems();
        AudienciaTipoDAO audienciaTipoDAO =  getDaoFactory().getAudienciaTipoDAO();
        Transaction tcbm = audienciaTipoDAO.getSession().getTransaction();
        tcbm.begin();
        List<AudienciaTipo> list = audienciaTipoDAO.findAll();
        Iterator<AudienciaTipo> it = list.iterator();
        while (it.hasNext()) {
            cbAudienciaTipo.addItem(it.next());
        }
    }    
