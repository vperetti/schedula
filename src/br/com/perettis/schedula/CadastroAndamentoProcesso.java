private void btnIncluirParcelaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIncluirParcelaActionPerformed
    if ((acompanhamento.getParcela() != null) && (!acompanhamento.getParcela().equalsIgnoreCase(""))) {
        JOptionPane.showMessageDialog(this, "Acompanhamento Vindo de Conversão, não pode ser alterado.");
        return;
    }
    if (txtParcelaValor.getText().length() <= 0) {
        JOptionPane.showMessageDialog(this, "Informe o Valor da Parcela");
        txtParcelaValor.requestFocus();
        return;
    }
    if (txtVencimento.getDate() == null) {
        JOptionPane.showMessageDialog(this, "Informe o Vencimento");
        txtVencimento.requestFocus();
        return;
    }
    AcordoParcela acordoParcela = new AcordoParcela();
    acordoParcela.setVencimento(txtVencimento.getDate());
    try {
        Double parcelaValor = Double.valueOf(formataParcela.parse(txtParcelaValor.getText()).doubleValue());
        acordoParcela.setParcelaValor(parcelaValor);
    } catch (ParseException ex) {
        Logger.getLogger(CadastroAndamentoProcesso.class.getName()).log(Level.SEVERE, null, ex);
    }
    acordoParcela.setAcompanhamento(acompanhamento);
//    acordoParcela.setParcelaValor(Double.valueOf(txtParcelaValor.getText()));

    //if (acompanhamento.getAcordoParcelas().size() == 0) {
    //    acordoParcela.setParcela("1/1");
    //    acompanhamento.getAcordoParcelas().add(acordoParcela);
    //}else{

    if (acordoParcelas.contains(acordoParcela)) {
        JOptionPane.showMessageDialog(this, "Vencimento já está lançado.");
        txtVencimento.requestFocus();
        return;
    }

    Set<AcordoParcela> listaNova = new LinkedHashSet<AcordoParcela>();
    int total = (acordoParcelas.size() + 1);
    int parcela = 1;
    boolean ja = false;
    for (Iterator it = acordoParcelas.iterator(); it.hasNext();) {
        AcordoParcela ver = (AcordoParcela) it.next();
        if (ver.getVencimento().equals(acordoParcela.getVencimento())) {
            JOptionPane.showMessageDialog(this, "Vencimento já está lançado.");
            txtVencimento.requestFocus();
            return;
        }
        if ((ja) || (ver.getVencimento().before(acordoParcela.getVencimento()))) {
            ver.setParcela(parcela++ + "/" + total);
            listaNova.add(ver);
        } else {
            acordoParcela.setParcela(parcela++ + "/" + total);
            listaNova.add(acordoParcela);
            ja = true;

            //ja coloca a proxima
            ver.setParcela(parcela++ + "/" + total);
            listaNova.add(ver);
        }
    }
    if (!ja) {
        acordoParcela.setParcela(parcela++ + "/" + total);
        listaNova.add(acordoParcela);
        ja = true;
    }
    acordoParcelas.clear();
    acordoParcelas.addAll(listaNova);
    //acompanhamento.setAcordoParcelas(listaNova);
    //}
    CarregaParcelas();
}//GEN-LAST:event_btnIncluirParcelaActionPerformed

