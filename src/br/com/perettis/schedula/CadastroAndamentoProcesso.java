    public void cbAcompanhamentoTipoModificacoes() {

        AcompanhamentoTipo acompanhamentoTipoSelecionado = ((AcompanhamentoTipo) cbAcompanhamentoTipo.getSelectedItem());

        if (acompanhamentoTipoSelecionado != null) {
            if (acompanhamentoTipoSelecionado.getDescricao().equalsIgnoreCase("Audiência")) {
                cbAudienciaTipo.setEnabled(true);
                cbAudienciaTipo.requestFocus();
            } else {
                cbAudienciaTipo.setSelectedIndex(-1);
                cbAudienciaTipo.setEnabled(false);
            }
            if (acompanhamentoTipoSelecionado.getDescricao().equalsIgnoreCase("Acordo")) {
                jTabbedPane2.setEnabledAt(1, true);
                //jTabbedPane2.setSelectedIndex(1);
            } else {
                jTabbedPane2.setEnabledAt(1, false);
            }
        }
    }
    
