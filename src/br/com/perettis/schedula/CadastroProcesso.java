    public void Pesquisar(){

        if (pesquisa == null) {
            pesquisa = new PesquisaProcesso(getDaoFactory());
        }
                
        pesquisa.setTitle("Selecione o Processo para Alteração.");
        pesquisa.doSearch();
        pesquisa.openSearch();
        Processo vem = pesquisa.getEntity();
        if (vem != null){
            txtId.setText(String.valueOf(vem.getId()));
            Alterar();
        }
    }

