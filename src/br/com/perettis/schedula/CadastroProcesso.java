    public void VaraCarrega(){
        if (txtIdVara.getText().length() <= 0) return;
        // Busca nova sessao
		VaraDAO varaDAO = daoFactory.getVaraDAO();
		try {
            // Inicia transação
			t = varaDAO.getSession().beginTransaction();
            
            // Busca objeto pelo id informado
            vara = varaDAO.findById(
                    Integer.valueOf(txtIdVara.getText()), false);
            
            // Preenche dados no formulário
            txtVaraNome.setText(vara.getNome());

		} catch (Exception e) {
            // Se não acha, nostra mensagem
			JOptionPane.showMessageDialog(this, 
                    resourceMap.getString("vara.errobusca", txtIdVara.getText()));
		}        
    }

